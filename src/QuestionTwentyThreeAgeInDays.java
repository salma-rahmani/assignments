/*
Question 23: Age in Days

    Create an int variable named ageInYears and assign it your age.
    Calculate your age in days by multiplying ageInYears by 365.
    Print your age in days.
 */
public class QuestionTwentyThreeAgeInDays {
    public static void main(String[] args) {
        int ageInYears = 30;
        int ageInDays = ageInYears * 365; // Calculate age in days
        System.out.println("My age in days is: " + ageInDays);
    }
}
