/*

Question 2: Working with Primitive Data Types

Step 1: Creating a Class and Main Method

    Create a new Java class named PrimitiveTypesDemo.
    Inside the PrimitiveTypesDemo class, create a main method.

Step 2: Declaring Primitive Variables

    Inside the main method, declare an int variable named intVar and initialize it with the value 100.
    Declare a float variable named floatVar and initialize it with the value 10.5f.
    Declare a char variable named charVar and initialize it with the value 'A'.

Step 3: Printing Primitive Variables

    Print the values of intVar, floatVar, and charVar using System.out.println.
 */
public class QuestionTwoPrimitiveTypesDemo {

    public static void main(String[] args) {

        // Step 2: Declaring Primitive Variables
        int intVar = 100;
        float floatVar = 10.5f;
        char charVar = 'A';

        // Step 3: Printing Primitive Variables using a single System.out.println
        System.out.println("The value of intVar is: " + intVar + ", the value of floatVar is: " + floatVar + ", and the value of charVar is: " + charVar);
    }
}
