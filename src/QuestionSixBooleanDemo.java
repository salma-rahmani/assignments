/*
Question 6: Using Boolean Variables

Step 1: Creating a Class and Main Method

    Create a new Java class named BooleanDemo.
    Inside the BooleanDemo class, create a main method.

Step 2: Declaring and Initializing Boolean Variables

    Inside the main method, declare a boolean variable named isJavaFun and initialize it with the value true.

Step 3: Using Boolean in Conditional Statements

    Write an if statement that prints "Java is fun!" if isJavaFun is true, otherwise prints "Java is not fun".
 */

public class QuestionSixBooleanDemo {

    public static void main(String[] args) {

        // Step 2: Declaring and Initializing Boolean Variables
        boolean isJavaFun = true;

        // Step 3: Using Boolean in Conditional Statements
        if (isJavaFun) {
            System.out.println("Java is fun!");
        } else {
            System.out.println("Java is not fun.");
        }
    }
}
